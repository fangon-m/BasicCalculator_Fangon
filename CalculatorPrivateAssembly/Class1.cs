using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculatorPrivateAssembly
{
    public class BasicComputation
    {
        public static float Add2(float num1, float num2)
        {
            return num1 + num2;
        }
        public static float Subtract2(float num1, float num2)
        {
            return num1 - num2;
        }
        public static float Multiply2(float num1, float num2)
        {
            return num1 * num2;
        }
        public static float Divide2(float num1, float num2)
        {
            return num1 / num2;
        }
    }
}
